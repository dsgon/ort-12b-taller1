package ar.edu.ort.taller1.tp5.Ejercicio2;

public class Jugador {
	
	private String nombre;
	private String nro;
	
	public Jugador (String nm, String n) {
		this.nombre = nm;
		this.nro = n;
	}
	
	public String getNombre() {
		return this.nombre;
	}
	
	public String getNro() {
		return this.nro;
	}

}
